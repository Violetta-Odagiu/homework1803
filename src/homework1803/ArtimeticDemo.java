package homework1803;

public class ArtimeticDemo {
        public static void main (String[] args) {
            int result = 1 + 2; // 3;
            System.out.println(result);

            result = result - 1; // 2;
            System.out.println(result);

            result = result * 2; // 0;
            System.out.println(result);

            result = result / 2; // 0;
            System.out.println(result);

            result = result + 8; // 8;
            result = result % 7; // 1'
            System.out.println(result);

        }

}
